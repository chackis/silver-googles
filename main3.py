from colorama import Fore, Back, Style # з колорами беремо штучки для зміни тексту
print(Fore.YELLOW + "Жойвтий текст") # Приклад використання параметру Fore для зміни кольору тексту
print(Back.CYAN + "Голубий фон") # Приклад використання параметру Back для зміни фону
print(Style.BRIGHT + "Яскравий стиль") # Приклад використання параметру Style для зміни стилю
